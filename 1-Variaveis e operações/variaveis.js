var a = 15; //Atribuindo valor a variavel 'a'
var b = 7; //Atribuindo valor a variavel 'b'
var result = a + b; // Atribuindo o resultado da soma de 'a' + 'b' a variavel 'result'
console.log(a + " + " + b + " = " + result); //imprimindo o resultado
/*Nota-se que a operação já foi feita antes da impressão do resultado
os sinais de positivo contidos na declaração do metodo 'console.log()'
são para a concatenação (Junção de tipos diferentes de variaveis) da impressão*/